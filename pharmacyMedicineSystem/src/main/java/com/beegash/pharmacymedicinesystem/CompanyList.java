    public void populateCompanyTable() {
        try {
            Connection connection = DBhelper.getConnection();
            PreparedStatement statement = connection.prepareStatement("SELECT * FROM companytable");
            Statement statement1 = connection.createStatement();
            ResultSet resultSet = statement1.executeQuery("SELECT * FROM companytable");

            while (resultSet.next()) {
                int companyId = resultSet.getInt("company_id");
                String companyName = resultSet.getString("company_name");
                String companyAddress = resultSet.getString("company_adress");
                String companyNumber = resultSet.getString("company_num");

                tableModel.addRow(new Object[]{companyId, companyName, companyAddress, companyNumber});
            }

            resultSet.close();
            statement1.close();
            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void refreshTable() {
        model.setRowCount(0);

        String sql = "SELECT * FROM companytable";
        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)});

            }

        } catch (Exception ex) {
            System.out.println("Error" + ex.getMessage());
        }
    }
