    private void btnAddCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCustomerActionPerformed
        // TODO add your handling code here:
        model.setRowCount(0);
        int id;
        String name = txtCompanyName.getText();
        String address = txtAdress.getText();
        String phone = txtPhone.getText();
        if (name.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a company name.");
            refreshTable();
            return;
        }

        for (Company cmpny : companyList) {
            if (cmpny.getCompanyName().equalsIgnoreCase(name)) {
                JOptionPane.showMessageDialog(this, "A company with the same name or id already exists.");
                refreshTable();
                return;
            }
        }
        try {
            id = Integer.parseInt(txtCompanyID.getText());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for id.");
            refreshTable();
            return;
        }

        if (address.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter an address.");
            refreshTable();
            return;
        }
        if (phone.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a phone number.");
            refreshTable();
            return;
        }

        Company company = new Company(id, name, address, phone);

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "1287");
            String query = "INSERT INTO companytable (company_id, company_name, company_adress, company_num) VALUES (?, ?, ?, ?)";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setInt(1, company.getCompanyId());
            stmt.setString(2, company.getCompanyName());
            stmt.setString(3, company.getCompanyAddress());
            stmt.setString(4, company.getCompanyPhoneNumber());

            stmt.executeUpdate();
            stmt.close();
            conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while adding the medicine to the database.");
            refreshTable();
            return;
        }
        companyList.add(company);
        String sql = "SELECT * FROM companyTable";
        try {
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new String[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)});

            }

        } catch (Exception ex) {
            System.out.println("Error" + ex.getMessage());
        }

        refreshTable();
    }//GEN-LAST:event_btnAddCustomerActionPerformed

    private void btnDeleteCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCustomerActionPerformed
        // TODO add your handling code here:

        int selectedRow = tableCustomer.getSelectedRow();
        if (selectedRow != -1) {
             String companyName = (String) model.getValueAt(selectedRow, 1);
            try {
                Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pharmacy?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "1287");
                String query = "DELETE FROM companytable WHERE company_name = ?";
                PreparedStatement stmt = conn.prepareStatement(query);
                stmt.setString(1, companyName);
                stmt.executeUpdate();
                stmt.close();
                conn.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "An error occurred while deleting the company from the database.");
                return;
            }

            companyList.remove(selectedRow);
            model.removeRow(selectedRow);

            JOptionPane.showMessageDialog(this, "Company sucsessfully removed");
        }
    }//GEN-LAST:event_btnDeleteCustomerActionPerformed

